public class Exercise_6_7
{
  public static void main( String[] args )
  {
    System.out.printf( "Absolute Value Of 7.5 Is %.1f\n", Math.abs( 7.5 ) );
    System.out.printf( "Floor Of 7.5 Is %.1f\n", Math.floor( 7.5 ) );
    System.out.printf( "Absolute Value Of 0.0 Is %.1f\n", Math.abs( 0.0 ) );
    System.out.printf( "Absolute Value Of -6.4 Is %.1f\n", Math.abs( -6.4 ) );
    System.out.printf( "Ceiling Of -6.4 Is %.1f\n", Math.ceil( -6.4 ) );
    System.out.printf( "Ceiling Of X Is %.1f\n", Math.ceil( -8.0 + Math.floor( -5.5 ) ) );
  }
}
