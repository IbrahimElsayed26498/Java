// Self_Review_2_14.java
// Displays Integers 1 through 4 with spaces in between

public class Self_Review_2_14
{
  public static void main(String[] args)
  {
    System.out.println("1 2 3 4");

    System.out.print("1");
    System.out.print(" 2");
    System.out.print(" 3");
    System.out.print(" 4\n");

    System.out.printf("%s %s %s %s", "1", "2", "3", "4\n");

  }
}
